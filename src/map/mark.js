class Mark {
    number;
    name;
    rarity;
    kind;
    imageUrl;
    position;

    constructor(number, name, rarity, kind, imageUrl, position) {
        this.number = number;
        this.name = name;
        this.rarity = rarity;
        this.kind = kind;
        this.imageUrl = imageUrl;
        this.position = position;
    }
}